export const LANG_KEY = "felixxii-lang";

const saved = (() => {
  try {
    const v = localStorage.getItem(LANG_KEY);
    return v === "en" || v === "vi" ? v : null;
  } catch {
    return null;
  }
})();

i18n.use(initReactI18next).init({
  resources: { en: { admin: en }, vi: { admin: vi } },
  lng: saved ?? "en",
  fallbackLng: "en",
  ns: ["admin"],
  defaultNS: "admin",
  interpolation: { escapeValue: false }, // react already escapes
});

// Persist the choice — otherwise every reload silently snaps back to English.
// `document` is guarded so the module can also be imported outside a browser
// (tests, scripts) without throwing at import time.
i18n.on("languageChanged", (lng) => {
  try {
    localStorage.setItem(LANG_KEY, lng);
    if (typeof document !== "undefined") document.documentElement.lang = lng;
  } catch { /* private mode */ }
});
if (typeof document !== "undefined") document.documentElement.lang = i18n.language;

export default i18n;
